    def run_ultra_optimized_loop(self):
        """Loop ULTRA-OPTIMIZADO con mejores fees y detecci√≥n"""
        from binance_api import market_data
        from itertools import combinations
        from strategies.triangular import simulate_route_gain, fetch_symbol_filters
        
        # Inicializar
        fetch_symbol_filters()
        sym_map = market_data.exchange_map()
        valid_symbols = set(sym_map.keys())
        
        print(f"\nüî• INICIANDO TRADING ULTRA-OPTIMIZADO")
        print(f"üìä Configuraci√≥n: {settings.QUANTUMS_USDT} USDT, {settings.PROFIT_THOLD*100:.2f}% m√≠n")
        print(f"üí∞ Fees optimizadas: 0.075% por transacci√≥n (con BNB)")
        
        cycle_count = 0
        adaptive_threshold = settings.PROFIT_THOLD
        opportunities_history = []
        
        while self.running:
            if TRADE_MONITOR_AVAILABLE and not trade_monitor.should_continue_trading():
                print("üõë Deteniendo trading por l√≠mites de seguridad")
                break
                
            cycle_start = time.time()
            cycle_count += 1
            
            try:
                # Obtener M√ÅS datos de mercado
                symbols = market_data.top_volume_symbols(settings.TOP_N_PAIRS)
                books = market_data.depth_snapshots(symbols[:35])  # M√°s s√≠mbolos
                
                # M√ÅS monedas prioritarias
                priority_coins = [
                    'BTC', 'ETH', 'BNB', 'ADA', 'DOT', 'LINK', 'XRP', 'LTC', 
                    'MATIC', 'AVAX', 'SOL', 'DOGE', 'ATOM', 'FIL', 'TRX'
                ]
                
                print(f"\nüîç CICLO {cycle_count} - Threshold: {adaptive_threshold*100:.2f}%")
                
                opportunities_found = 0
                best_opportunities = []
                
                # üî• B√öSQUEDA ULTRA-AGRESIVA de oportunidades
                for combo in combinations(priority_coins[:10], 2):  # M√ÅS combinaciones
                    if not self.running:
                        break
                        
                    route = [settings.BASE_ASSET] + list(combo) + [settings.BASE_ASSET]
                    
                    # üéØ Probar TODAS las cantidades de settings
                    for amount in settings.QUANTUMS_USDT:
                        try:
                            final_qty = simulate_route_gain(route, amount, books, valid_symbols)
                            if final_qty == 0:
                                continue
                            
                            # üí° USAR NUESTRO NUEVO C√ÅLCULO DE FEES
                            fee_analysis = calculate_net_profit_after_fees(route, amount, final_qty)
                            
                            # Solo continuar si es rentable DESPU√âS de fees
                            if not fee_analysis['profitable']:
                                continue
                                
                            net_profit = fee_analysis['net_profit']
                            net_profit_pct = fee_analysis['net_profit_percentage'] / 100
                            
                            # üéØ Usar threshold m√°s inteligente
                            if net_profit_pct > adaptive_threshold:
                                opportunities_found += 1
                                
                                # Score de calidad mejorado
                                quality_score = min(1.0, net_profit_pct / 0.01)  # Normalizar a 1%
                                
                                opportunity = {
                                    'route': route,
                                    'amount': amount,
                                    'gross_profit': final_qty - amount,
                                    'net_profit': net_profit,
                                    'net_profit_pct': net_profit_pct,
                                    'total_fees': fee_analysis['total_fees'],
                                    'quality_score': quality_score,
                                    'fee_analysis': fee_analysis
                                }
                                
                                best_opportunities.append(opportunity)
                                
                                # Log mejorado
                                if TRADE_MONITOR_AVAILABLE:
                                    trade_monitor.log_opportunity(route, amount, net_profit, quality_score)
                                
                        except Exception as e:
                            logging.debug(f"Error evaluando {route}: {e}")
                            continue
                
                # Ordenar por rentabilidad NETA
                best_opportunities.sort(key=lambda x: x['net_profit_pct'], reverse=True)
                
                # üî• Ejecutar mejores oportunidades con validaci√≥n avanzada
                trades_executed = 0
                for opp in best_opportunities[:5]:  # Top 5 en lugar de 3
                    
                    print(f"üí∞ OPORTUNIDAD VALIDADA: {' ‚Üí '.join(opp['route'])}")
                    print(f"   üíµ {opp['amount']:.0f} USDT ‚Üí Ganancia NETA: +{opp['net_profit']:.4f} USDT ({opp['net_profit_pct']*100:.3f}%)")
                    print(f"   üí∏ Fees totales: {opp['total_fees']:.4f} USDT")
                    print(f"   üéØ Quality: {opp['quality_score']:.2f}")
                    
                    # üéØ Validaci√≥n FINAL con nuestro sistema mejorado
                    trade_decision = should_execute_trade_with_fees(
                        opp['route'], opp['amount'], opp['amount'] + opp['gross_profit']
                    )
                    
                    if trade_decision['should_execute'] and opp['quality_score'] >= 0.2:
                        print(f"   ‚úÖ {trade_decision['recommendation']}: {trade_decision['reason']}")
                        
                        success, actual_profit = self.execute_real_trade(
                            opp['route'], opp['amount'], opp['net_profit'], books
                        )
                        
                        if success:
                            trades_executed += 1
                            print(f"   üéâ Trade ejecutado: +{actual_profit:.4f} USDT")
                        
                        if TRADE_MONITOR_AVAILABLE:
                            trade_monitor.log_trade_execution(
                                opp['route'], opp['amount'], success, actual_profit,
                                execution_time=2.0,
                                error_msg="" if success else "Error de ejecuci√≥n"
                            )
                        
                        if success:
                            time.sleep(0.5)  # Pausa m√°s corta
                    else:
                        print(f"   ‚ùå RECHAZADO: {trade_decision['reason']}")
                
                # üéØ Ajuste adaptativo MEJORADO del threshold
                opportunities_history.append(opportunities_found)
                if len(opportunities_history) > 15:  # M√°s historial
                    opportunities_history = opportunities_history[-15:]
                
                avg_opportunities = sum(opportunities_history) / len(opportunities_history)
                
                # üî• L√≥gica de ajuste M√ÅS AGRESIVA
                if avg_opportunities < 0.3:
                    # MUY pocas oportunidades: reducir threshold M√ÅS agresivamente
                    adaptive_threshold *= 0.85
                    adaptive_threshold = max(0.001, adaptive_threshold)  # M√≠nimo 0.1%
                    print(f"üéØ Threshold ULTRA-reducido a {adaptive_threshold*100:.2f}%")
                elif avg_opportunities < 1:
                    # Pocas oportunidades: reducir threshold
                    adaptive_threshold *= 0.92
                    adaptive_threshold = max(0.0015, adaptive_threshold)  # M√≠nimo 0.15%
                    print(f"üéØ Threshold reducido a {adaptive_threshold*100:.2f}%")
                elif avg_opportunities > 6:
                    # MUCHAS oportunidades: aumentar threshold
                    adaptive_threshold *= 1.15
                    adaptive_threshold = min(0.025, adaptive_threshold)  # M√°ximo 2.5%
                    print(f"üéØ Threshold aumentado a {adaptive_threshold*100:.2f}%")
                
                # Mostrar estad√≠sticas M√ÅS frecuentes
                if cycle_count % 3 == 0:  # Cada 3 ciclos en lugar de 5
                    if TRADE_MONITOR_AVAILABLE:
                        trade_monitor.show_live_stats()
                    print(f"üìà Oportunidades promedio: {avg_opportunities:.1f}")
                    print(f"üìä Threshold actual: {adaptive_threshold*100:.2f}%")
                    print(f"üí∞ Trades ejecutados este ciclo: {trades_executed}")
                
                # ‚ö° Pausa M√ÅS adaptativa y r√°pida
                cycle_time = time.time() - cycle_start
                if opportunities_found == 0:
                    sleep_time = max(0.5, settings.SLEEP_BETWEEN * 0.7)  # M√ÅS r√°pido si no hay oportunidades
                elif opportunities_found > 3:
                    sleep_time = max(0.8, settings.SLEEP_BETWEEN * 1.1)  # Poco m√°s lento si hay muchas
                else:
                    sleep_time = max(0.5, settings.SLEEP_BETWEEN - cycle_time)
                
                time.sleep(sleep_time)
                
            except Exception as e:
                logging.error(f"‚ùå Error en ciclo: {e}")
                time.sleep(1)  # Pausa m√°s corta en errores
    
    def execute_real_trade(self, route, amount, expected_profit, books):
        """Ejecuta un trade real con validaciones adicionales"""
        try:
            if not self.live_mode:
                # Simulaci√≥n M√ÅS realista
                return True, expected_profit * 0.85  # 85% de la ganancia esperada
            
            # AQU√ç VA LA EJECUCI√ìN REAL
            print(f"üî• EJECUTANDO TRADE REAL: {' ‚Üí '.join(route)}")
            
            if ENHANCED_MODULES:
                # Usar order executor avanzado
                execution_result = order_executor.execute_arbitrage_atomic(
                    route, amount, max_slippage=0.015
                )
                
                return execution_result.success, execution_result.net_profit
            else:
                # Ejecuci√≥n b√°sica
                from strategies.triangular import execute_arbitrage_trade
                execute_arbitrage_trade(route, amount)
                
                # Resultado m√°s realista
                actual_profit = expected_profit * 0.75  # 75% conservador
                return True, actual_profit
                
        except Exception as e:
            logging.error(f"‚ùå Error ejecutando trade real: {e}")
            return False, 0.0

    def setup_signal_handlers(self):
        """Configura cierre limpio"""
        def signal_handler(sig, frame):
            print("\nüõë Cerrando bot de trading...")
            self.shutdown()
            sys.exit(0)
        
        signal.signal(signal.SIGINT, signal_handler)
        signal.signal(signal.SIGTERM, signal_handler)
    
    def shutdown(self):
        """Cierre limpio con reporte final"""
        self.running = False
        
        if TRADE_MONITOR_AVAILABLE:
            print("\nüìä REPORTE FINAL DE SESI√ìN")
            print("="*60)
            summary = trade_monitor.get_session_summary()
            
            print(f"‚è±Ô∏è Duraci√≥n: {summary['session_duration_hours']:.1f}h")
            print(f"üéØ Oportunidades/hora: {summary['opportunities_per_hour']:.1f}")
            print(f"üöÄ Trades/hora: {summary['trades_per_hour']:.1f}")
            print(f"üí∞ Ganancia neta: {summary['net_profit']:+.4f} USDT")
            print(f"‚úÖ Tasa de √©xito: {summary['success_rate']:.1f}%")
            print(f"üèÜ Mejor trade: +{summary['best_trade']:.4f} USDT")
            print("="*60)
        
        print("üëã Bot cerrado correctamente")
    
